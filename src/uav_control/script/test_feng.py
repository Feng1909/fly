def plus(q1, q2):
    # w, x, y, z
    return [q1[0]*q2[0]-q1[1]*q2[1]-q1[2]*q2[2]-q1[3]*q2[3],
            q2[0]*q1[1]+q1[0]*q2[1]+q1[3]*q2[2]-q1[2]*q2[3],
            q2[0]*q1[2]-q1[3]*q2[1]+q1[0]*q2[2]+q1[1]*q2[3],
            q2[0]*q1[3]+q1[0]*q2[3]-q1[1]*q2[2]+q1[2]*q2[1]]

print(plus([ 0.9914449, 0.1305262, 0, 0 ],[  0.9659258 ,0.258819, 0, 0]))
